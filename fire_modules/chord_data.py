def render_text_to_bitmap(text, scroll_offset=0):
    """Render text string to a 16x4 bitmap for the pad matrix.
    Returns a list of 4 rows, each row is a list of 16 values (0 or 1).
    scroll_offset shifts the rendered text left (for scrolling)."""

    # First, build the full pixel strip for the text
    full_strip = [[], [], [], []]  # 4 rows

    for i, ch in enumerate(text):
        glyph = FONT_4PX.get(ch, FONT_4PX.get(' '))
        if glyph is None:
            glyph = FONT_4PX[' ']
        # Add char pixels
        for row in range(4):
            if row < len(glyph):
                full_strip[row].extend(glyph[row])
            else:
                full_strip[row].extend([0] * len(glyph[0]))
        # Add 1px spacing between chars (except after last)
        if i < len(text) - 1:
            for row in range(4):
                full_strip[row].append(0)

    # Apply scroll offset and extract 16 columns (left-aligned)
    result = []
    for row in range(4):
        row_data = []
        for col in range(16):
            src_col = col + scroll_offset
            if 0 <= src_col < len(full_strip[row]):
                row_data.append(full_strip[row][src_col])
            else:
                row_data.append(0)
        result.append(row_data)

    return result


def get_text_pixel_width(text):
    """Get the total pixel width of rendered text."""
    width = 0
    for i, ch in enumerate(text):
        glyph = FONT_4PX.get(ch, FONT_4PX.get(' '))
        if glyph is None:
            glyph = FONT_4PX[' ']
        width += len(glyph[0])
        if i < len(text) - 1:
            width += 1  # spacing
    return width
